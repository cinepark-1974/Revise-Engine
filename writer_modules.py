def get_genre_enforcement(genre: str) -> str:
    """
    장르별 매 비트 강제 체크 블록 반환.
    복합 장르는 Creator Engine/Writer Engine의 '두 번째 장르 = 본질' 법칙에 따라
    본질 장르 하나의 체크만 주입 (메시지 중첩 방지).
    """
    dna = _resolve_opening_dna(genre)
    blocks = []

    # 로맨틱 코미디 특수 처리 — 두 번째 장르가 코미디면 ROMCOM_ENFORCEMENT
    if _is_comedy(genre) and _is_romance(genre):
        blocks.append(ROMCOM_ENFORCEMENT)
    elif dna == "comedy":
        blocks.append(ROMCOM_ENFORCEMENT)
    elif dna == "romance":
        # 순수 로맨스 — ROMCOM_ENFORCEMENT의 대사 강제는 유지하되 웃음 강제는 완화
        blocks.append(ROMCOM_ENFORCEMENT)
    elif dna == "drama":
        blocks.append(DRAMA_ENFORCEMENT)
    elif dna == "thriller":
        blocks.append(THRILLER_ENFORCEMENT)
    elif dna == "horror":
        blocks.append(HORROR_ENFORCEMENT)
    elif dna == "action":
        blocks.append(ACTION_ENFORCEMENT)
    # sf/fantasy는 드라마 강제 체크 적용
    else:
        blocks.append(DRAMA_ENFORCEMENT)

    return "\n\n".join(blocks) if blocks else ""
