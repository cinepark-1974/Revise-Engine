def get_historical_film_rules(historical: bool, film_type: str = "") -> str:
    """
    역사영화 규칙 블록 반환.
    
    Args:
        historical: 역사영화 체크 여부
        film_type: "정통" / "팩션" / "퓨전" (또는 영문/일부 일치 허용)
    
    Returns:
        체크 OFF면 빈 문자열. ON이면 BASE + 선택 유형 블록 반환.
    """
    if not historical:
        return ""
    
    parts = [HISTORICAL_FILM_BASE]
    
